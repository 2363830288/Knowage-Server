package it.eng.spagobi.api.dto;

import java.util.ArrayList;
import java.util.List;

/**
 * @author Marco Libanori
 */
public class ViewFolder {

	private String id;

	private String parentId;

	private String name;

	private String description;

	private Integer progr;

	private final List<ViewFolder> children = new ArrayList<>();

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ViewFolder other = (ViewFolder) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	/**
	 * @return the subFolders
	 */
	public List<ViewFolder> getChildren() {
		return children;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @return the parentId
	 */
	public String getParentId() {
		return parentId;
	}

	/**
	 * @return the progr
	 */
	public Integer getProgr() {
		return progr;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @param parentId the parentId to set
	 */
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}

	/**
	 * @param progr the progr to set
	 */
	public void setProgr(Integer progr) {
		this.progr = progr;
	}

	@Override
	public String toString() {
		return "ViewFolder [id=" + id + ", name=" + name + ", children=" + children + "]";
	}
}
